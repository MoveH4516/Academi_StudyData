package 기초1문제1;
/*
	[문제]	  	
		철수는 학용품을 정리하고 있다.
		가지고 있는 지우개와 볼펜과 연필을 모두 합치니 27개였다.
		지우개는 12개이고 볼펜이 7개라고 하면, 
		연필은 모두 몇 개인지 구하시오.
	[정답] 
		8
*/
public class _문제02연필 {
	public static void main(String[] args) {
		
		/*
		 * [풀이] 지우개 + 볼펜 + 연필 = 27 인데 지우개가 12 볼펜이 7 이므로 12 + 7 + 연필 = 27이다 
		 * 따라서 연필 = 27 - 12 - 7 이므로 연필은 8개 이다.
		*/
		System.out.println(27 - 12 - 7);
		
	}
}
