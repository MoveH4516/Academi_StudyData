package 기초1개념1;

public class _07산술연산자 {
	public static void main(String[] args) {
		
		// 연산자 ( operator ) 
		// +, -, *, /, % 
		System.out.println(10 + 2);
		System.out.println(10 - 2);
		System.out.println(10 * 2);
		System.out.println(10 / 3); // 몫의 값을 출력
		System.out.println(10 % 6); // 나머지 계산
	}
}
