package 기초1개념1;

public class _10소숫점처리 {
	public static void main(String[] args) {
		
		//소숫점을 올림 or 내림 or 반올림을 통해 정리를 해주면 좋다
		System.out.println(11/3.0);
		System.out.println(Math.ceil(11/3.0)); // 올림
		System.out.println(Math.floor(11/3.0)); //내림
		System.out.println(Math.round(11/3.0)); //반올림
		
	}
}
